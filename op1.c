/*Operators:
    Arithmetic Operators:
        Addition:   +
        Subtraction: -
        Multiplication: *
        Division: /
        Modulo: %  (reminder of division)*/
    #include<stdio.h>
    int main()
    {
        int a=10,b=3;
        printf("Sum of a & b: %d\n",a+b);
        printf("Difference of a & b = %d\n",a-b);
        printf("Multiplication: %d\n",a*b);
        printf("Quotient of a / b = %d\n",a/b);
        printf("Reminder of a/b = %d",a%b);
    }