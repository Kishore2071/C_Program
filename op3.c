/*
Relational operators
   less than:  <
   greater than : >
   less than or equal : <=
   greater than equal : >=
   not equal:    !=
   both equal or not :  == 
   */
  #include<stdio.h>
  int main()
  {
      int a=6, b=4;
      printf("A==B = %d", a==b);
      printf("A<B=%d\n",a<b);
      printf("A>B = %d\n",a>b);
      printf("A!=B %d",a!=b);
  }